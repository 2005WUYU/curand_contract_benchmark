def spec_by_id() -> dict[str, TaskSpec]:
    return {spec.task_id: spec for spec in build_task_specs()}


def specs_for_groups(groups: set[str]) -> list[TaskSpec]:
    specs = build_task_specs()
    if "all" in groups:
        return specs
    selected: list[TaskSpec] = []
    for spec in specs:
        if spec.task_id in groups or spec.family in groups or f"stage{spec.stage}" in groups:
            selected.append(spec)
    return selected
